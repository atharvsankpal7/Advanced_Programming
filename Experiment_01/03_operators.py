num1=10;
num2=3;
print("num1 + num2 = ",num1+num2);
print("num1 - num2 = ",num1-num2);
print("num1 * num2 = ",num1*num2);
print("num1 / num2 = ",num1/num2);
#print("num1 / num2 = ",int(num1/num2));
print("num1 % num2 = ",num1%num2);
print("num1 // num2 = ",num1//num2);#Floor division operator
print("num1 ** num2 = ",num1**num2);#Exponential operator


